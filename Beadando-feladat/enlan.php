    <br>
    <h3 class="szoveg2-4">
    Elérkeztünk hát ahhoz az eszközhöz, ami miatt alapvetően az EW042-est megrendeltem. Erről sok érdekességet nem tudok mondani, csak annyit, 
    hogy működik a dolog: az ilyen tengelycsavarral ellátott Enlan bicskáinkat könnyebben tarthatjuk karban a segítségével. Annyi kritikai észrevételem 
    lenne, hogy nem árt óvatosan bánni vele, mert ha nem megfelelő erővel szorítjuk rá a csavarfejre, könnyen elugrik, és megkarcolhatja akár magát a csavart, 
    akár a markolatot.
