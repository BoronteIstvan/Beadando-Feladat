    <h3 class="szoveg1-2">
    Enlan EW042, a kabát
    <br>
    <br>
    <h3 class="szoveg2-4">
    Ahogy fentebb már írtam, a késre elsősorban csak a szerszám kiegészítőjeként gondoltam. 
    A képek alapján egy dekoratív, masszív foldert vártam, és ebben nem is kellett csalódnom. Sajnos 
    azonban a kés tervezője nem állt meg ott, ahol szerintem meg kellett volna, és – ki tudja, miért – 
    elhelyezett a markolat felső részén egy végigfutó, agresszív recézést. Persze, a recézés stabilabb fogást 
    ad, ami az alumínium markolatnál kifejezetten előnyös, de ez a megoldás már ránézésre is kényelmetlen. Sajnos 
    nem csak ránézésre: simán kézbe véve, és egy kicsit keményebben megmarkolva is az, pedig dolgozni még el sem kezdtünk vele.
    <br>
    <br>
    Őszinte leszek, nekem már itt elment egy kicsit a kedvem attól, hogy alaposabb tesztet írjak a késről, de ha már nálam van, 
    gondoltam, néhány gondolatot megér azért. A külcsínről érintőlegesen már volt szó: szerintem alapvetően nem néz ki rosszul a kés. 
    Mind a formavilága, mind az anyagválasztás egy kissé futurisztikus, simán el tudnám képzelni, hogy valami sci-fi filmben az űrhajón 
    ezzel szeli valaki a szintetikus szalonnát. A penge kifejezetten jól néz ki, erős, jól használható munkakés benyomását kelti a drop point 
    formájával, a hamis fokéllel és a magasra felköszörült szablyaéllel. Kapunk még egy enyhe, de azért használható ujjvájatot is a pengetőnél, 
    amit én kifejezetten kedvelek.
    <br>
    <br>
    Az ezüstszínű markolat az íves felületkialakításával szintén mutatós, engem valamiféle ragadozóra emlékeztet, különösen a 
    markolatvég kialakítása miatt. Még a már emlegetett recézéssel sem lenne gond, bár nekem ez már egy kicsit sok vizuálisan, 
    de ahogy már kitértem rá, nagyon kényelmetlenre sikerült. Ráadásul az alumínium panelek csúszósságán nem sokat segít, már 
    enyhén izzadt vagy vizes kézzel is instabilnak éreztem a fogását. A 17mm-es vastagság is túlzó szerintem, számomra már ez 
    is kényelmetlenné tette egy kissé a kés fogását.
