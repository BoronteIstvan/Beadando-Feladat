    <h3 class="szoveg1-2">
    Vágjunk bele, avagy vágjunk vele!
    <br>
    <br>
    <h3 class="szoveg2-4">
    Sajnos nem lett ez szerelem első látásra, de azért nézzük meg, hogy vágni lehet-e vele! Hát, alapvetően lehet. 
    Sajnos a kés a dobozból kivéve nem elég éles, a fénymásolópapírt már csak akadozva szeli. A kartonpapírral is 
    meggyűlt egy kissé a baja, ahol a többi kés vidáman dolgozott, az EW042 sokszor szenvedett, és inkább gyűrte – vagy tépte – az anyagot vágás helyett.
    <br>
    <br>
    Egy kicsit azért farigcsálni is próbáltam, ahol viszont meglepően szépen dolgozott, kunkorította a faforgácsot rendesen. Már épp kezdtem volna élvezni a 
    dolgot, de néhány másodpercen belül a fogása nagyon kényelmetlenné vált. Talán kesztyűben nem okozna ez sem gondot, de puszta kézzel azért nem ez lesz a 
    jövőben a kedvenc tevékenységem. Némi pozitívum, hogy a 8Cr13MoV pengeanyag jól karbantartható, így a próbálgatás után a szokásos Spyderco Sharpmakeres 
    módszerrel percek alatt sikerült egy, a gyárinál jobb élt varázsolnom rá.
    <br>
    <br>
    Ha épp nem dolgoznánk a késsel, de szeretnénk, ha kéznél lenne, az Enlan EW042 természetesen kapott egy zsebklipet is, amivel heggyel lent, jobb oldalon 
    hordhatjuk. Sajnos a klip a legnagyobb jóindulattal sem nevezhető deep carrynek, jó 25mm-nyi markolatanyag ki fog lógni a zsebünkből. Ezen a téren is 
    lehetett volna egy kicsit jobb a kés, azt gondolom.
