    Ami a mechanikát illeti, az még az épp elmegy kategória. A nyitópöcökkel viszonylag könnyen lehet nyitni a pengét, bár egy csepp olaj szükséges ahhoz, hogy igazán gördülékenyen működjön. 
    A penge nyitott állapotban stabil, minimális lötyögést, mozgást sem tapasztaltam, csukva pedig hajszál híján középre zár. A liner lock egyébként biztosan tartja a pengét, ugyanakkor az én 
    példányomon kissé késői a zárás, sokszor alig tudtam oldani egy határozottabb nyitást követően.
