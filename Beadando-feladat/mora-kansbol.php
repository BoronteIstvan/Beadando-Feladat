    <h3 class="szoveg1-2">
    Mora Kansbol
    <h3 class="szoveg2-1">
    Szerző: Vaszilij | dec 15, 2019 | Kések, Fixpengés, Mora
    <br>
    <br>
    <img class="responsive-img1" src="images/kesek/mora/mora-kansbol1.png" alt="Mora-Kansbol" width="700" height="500">
    <br>
    <br>
    <h3 class="szoveg2-4">
    A Mora Kansbol a svéd gyártó újabb modellekből álló triumvirátusának egyik tagja. A Garberg és az Eldris után sokat gondolkodtam, 
    hogy kell-e nekem ez a modell is, és végül úgy döntöttem, hogy miért ne? Az ára nem túl vészes, az interneten elég sok pozitív véleményt 
    találni róla. Az utolsó lökés az volt, amikor kijött a tesztben is szereplő Burnt Orange színű verzió: nem tétováztam tovább, rendeltem egyet magamnak.
    <br>
    <br>
    <h3 class="szoveg1-2">
    Műszaki adatok
    <br>
    <br>
    <h3 class="szoveg2-4">
    Pengehossz: 109 mm
    <br>
    Markolathossz: 117 mm
    <br>
    Teljes hossz: 226 mm
    <br>
    Pengevastagság: 2,5 mm
    <br>
    Penge anyaga: 12C27
    <br>
    Markolat anyaga: Poliamid/gumi
    <br>
    Tömeg: 188g
    <br>
    <h3 class="szoveg1-2">
    Külcsín
    <br>
    <br>
    <h3 class="szoveg2-4">
    Ahogy a másik két újabb Mora-modell, úgy a Kansbol is őriz valamit az elődök formavilágából,
    ugyanakkor kevésbé tűnik gazdaságosnak vagy túlzottan letisztultnak. Semmiképp nem nevezhetjük 
    túldizájnoltnak a kést, mégis számos apró részletet találunk rajta, ami arról árulkodik, hogy alapos 
    és tudatos tervezés eredményével van dolgunk.
    <br>
    <br>
    Az elsőre semlegesnek tűnő markolat kitűnően illik a kézbe, a kétkomponensű anyaghasználat, és a 
    műanyag betét textúrája pedig még stabilabb fogást eredményez. Ugyanebben segít a penge előtti kiszélesedés, 
    ami ujjtámaszként is funkcionálhat, illetve a markolatvég madárfej formája. Ez utóbbi akkor is jó szolgálatot
    tehet, ha csapkodni szeretnénk a késsel, habár a tömegéből és méretéből adódóan nem elsődlegesen erre készült a Kansbol.
    <br>
    <br>
    Amennyiben mégis befognánk ilyesmire, a markolat végén kapunk egy zsinórfuratot is, amivel biztonságosabbá tehetjük a csapkodást.
    <br>
    <br>
    A clip point-szerű penge kétféle leélezést kapott. A markolathoz közelebb egy szélesebb, vasagabb skandi éllel, míg a hegyhez 
    közelebb egy elvékonyított leélezéssel látták el. A penge egyébként alapból nem túl vastag, mindössze 2,5 mm-es, ebből – és a 
    markolattüskés kialakításból – sejthetjük, hogy nem kifejezetten durva feladatokra szánták.
    <br>
    <br>
    <img class="responsive-img1" src="images/kesek/mora/mora-kansbol2.png" alt="Mora-Kansbol" width="700" height="500">
    <br>
    <br>
    <h3 class="szoveg1-2">
    Belbecs
    <h3 class="szoveg2-4">
    <br>
    <br>
    Fixpengés késről lévén szó, itt túl sokat nem tudok elmondani, főképp, hogy az anyaghasználatot már részben érintettem 
    egy picit fentebb. Amiről nem beszéltem, az a penge anyaga. A Kansbol 12C27 acéllal készült, ami szerintem nem rossz az 
    árához képest, habár a nagyobb tesónál használt 14C28N egy kicsit jobban megdobogtatta volna a késbarátok szívét.
    <br>
    <br>
    Ahogy szintén említettem már, a kés markolattüskés kialakítású. Ha valaki tényleg durva feladatokra szánja, akkor 
    talán kevés lehet, de miután egy Companion is elég sokat kibír, azt gondolom, hogy egy átlagos felhasználó számára 
    bőven elég lehet, még ha néha ütőfázásra is szeretné használni.
    <br>
    <br>
    <h3 class="szoveg1-2">
    A fotelrambó és a Mora Kansbol kalandjai
    <br>
    <br>
    <h3 class="szoveg2-4">
    Bajban vagyok, amikor fixpengés késeket – főképp túra- vagy bushcraft modelleket – szeretnék bemutatni. 
    Mindig hangsúlyozom, hogy nem vagyok szakértője a témának, a fixek mintegy a késes hobbi mellékágaként 
    jöttek számomra. Így aztán fogalmam sincs, hogy számotokra nyújtanak-e bármiféle érdemi információt az amatőr tesztjeim.
    <br>
    <br>
    Ennek ellenére néhányszor magammal vittem a Kansbolt erdei kirándulásaimra, és próbálgattam, hogy milyen vele dolgozni. 
    A legfontosabb, amit tapasztaltam vele kapcsolatban, hogy a markolat meglehetősen kényelmes. Akár tollaságat faragtam, 
    akár nyársat hegyeztem vele, a kés fogása végig komfortos maradt. A Garberget még így is kényelmesebbnek érzem egy árnyalatnyival, 
    de szerintem a Kansbol is kitűnően teljesít.
    <br>
    <br>
    Ami a vágási teljesítményt illeti, nekem szoknom kellett a kést. A kettős leélezés, és a Morákra jellemző „skandi élszalaggal” 
    ellátott él eleinte picit furcsa volt, de végül azért meg tudtam vele csinálni, amit szerettem volna. Amikor a konyhában is kipróbáltam,
    azt tapasztaltam, hogy a kés hegy felőli része kitűnően dolgozik, de valahogy nem volt teljesen természetes vele a munka, mivel a durvábban
    élezett rész, és a kettő közötti átmenet furán működött. Nyilván, ha sokat használjuk a kést, és jobban ráérzünk, könnyebben megy a dolog,
    de nekem elsőre valahogy természetellenes volt vele a munka.
    <br>
    <br>
    Természetesen az ütőfázást is kipróbáltam. Ez sem jelentett gondot a Kansbolnak, habár a Garberg vastagabb pengéje jobban repeszti az anyagot. 
    Problémám ezzel a késsel sem volt, a faaprítás végeztével a kés még mindig kitűnő egészségnek örvendett.
