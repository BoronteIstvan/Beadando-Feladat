    <h3 class="szoveg1-2">
    Hordhatóság és karbantartás
    <br>
    <br>
    <h3 class="szoveg2-4">
    A Kansbol kétféle kiszerelésben vásárolható meg. Az egyszerűbb verzió, amelyet én is birtoklok, egy egyszerű övbújtatóval érkezik, 
    amelynek segítségével övön hordható a kés. Kapható egy olyan változat is, amely egy úgynevezett Multi Mount rendszert is tartalmaz, 
    amivel sokféleképpen, különböző állasokban szerelhetjük fel a kést akár a ruházatunkra, akár a táskánkra.
    <br>
    <br>
    Maga a tok egyszerű műanyag, de a funkcióját tökéletesen ellátja. Jobb- és baloldalon is hordható. Erősen, biztonságosan tart, 
    így nem kell félnünk attól, hogy elhagyjuk a kést. Amit hiányoltam róla, az valamiféle ujjtámasz, ami a kés elővételekor segíthet, 
    nekem egy picit így macerás volt a használata.
    <br>
    <br>
    Ami a karbantartást illeti, én egy Sharpmaker segítségével éleztem meg használat után. A mikro élszalag miatt működik a dolog, 
    nálam profibb és igényesebb éltársak persze kialakíthatnak rajta hagyományos skandi élt is. A 12C27 egy hálás acél, jól élezhető, és 
    relatív jól ellenáll a korróziónak is, a jó értelemben igénytelen markolat és tok pedig egyszerűen tisztítható, és különösebb ápolást 
    sem igényel.
