    <h3 class="szoveg1-2">
    Összegzés
    <br>
    <br>
    <h3 class="szoveg2-4">
    A Mora Kansbol szerintem egy jól eltalált kés, ha egy nem túl drága, mégis minőségi darabot szeretnénk a 
    felszerelésünkbe kirándulásokhoz, túrákhoz. Az ára sem vészes, az övbújtatós verziót kedvezménnyel egy picivel tízezer forint alatt megvásárolhatjuk.
    <br>
    <br>
    Hogy van-e létjogosultsága ennek a modellnek az igazán olcsó Morák vagy a Garberg mellett, esetleg egy Eldrishez, már egy fogósabb kérdés. Nekem –
    főleg ebben a színben – megtetszett, és a jól illik a túracipőmhöz is.
