    <h3 class="szoveg1-2">
    Hordhatóság
    <br>
    <br>
    <h3 class="szoveg2-4">
    Ezen a téren sajnos kevésbé vagyok elégedett. A késhez mellékelt bőr tok méltatlanul gyenge. 
    Sima erszénytokról van szó, ami ugyan tartja a kést, de mintha három-négy centivel hosszabb 
    lenne a kelleténél, illetve maga a bőr is valamilyen olcsó megoldás, mert hamar elkezdett megkopni.
    <br>
    <br>
    Az igazi feketeleves a felfogási módja. Egyfajta övre fűzős megoldást kapunk, ami az alsó felén patentos, 
    így relatív könnyen fel- és leszerelhetjük. Ezzel a módszerrel számomra kényelmetlenül magasra esik a kés,
     és bár még használható, de hidegebb időben, sok ruha alól biztos, hogy macerás lenne előszedni. Kérdés még, 
     hogy ez a patent mennyire tartós, el tudom képzelni, hogy a gyakorlatban, rendszeres használat mellett nem lenne hosszú életű.
    <br>
    <br>
    Márpedig bármennyire jó egy kés, ha a tok a gyenge láncszem, az az egész csomagra jelentős hatást gyakorol. Természetesen 
    ha valaki hajlandó a zsebébe nyúlni, egy egyedi bőr- vagy kydex tok csodákat képes művelni.
