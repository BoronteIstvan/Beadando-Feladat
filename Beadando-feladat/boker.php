    <h3 class="szoveg1-2">
    Bushcraft, Outdoor, miegymás
    <br>
    <br>
    <h3 class="szoveg2-4">
    Gyakran poénkodom azzal, hogy afféle Fotelrambó vagyok. Időnként ugyan kimerészkedem a 
    négy fal közül, de az esetek nagy többségében elég nekem ilyenkor is egy folder, plusz 
    minden eshetőségre a hátizsákban egy olcsó Mora. Miért van az, hogy időről-időre mégis beszerzek 
    egy-egy, alapvetően outdoor felhasználásra szánt darabot?
    <br>
    <br>
    Az egyik válasz a kíváncsiság. Mivel jó ideje foglalkozom késekkel, az érdeklődési köröm elég sokat bővült a 
    kezdetek óta. A modern folderek után igyekeztem többféle szegmensben körülnézni, így jöttek sorra a kisebb és 
    nagyobb fixek, automata kések, sőt volt, hogy egy karambitot is beszereztem. Úgy gondolom, amikor új késeket próbál 
    ki az ember, nem csak azokról és a késes korszellemről szerez tapasztalatokat, hanem önmagáról is. Jobban képbe kerül a 
    szükségleteivel, az igényeivel, a lehetőségeivel kapcsolatban, ha pedig elég időt hagy magának a tapasztalásra, fejlődni 
    is tud ezen a téren.
    <br>
    <br>
    A második indok ennél sokkal prózaibb: hobbistaként az ember néha irracionális döntéseket hoz. Mert egy idő után nem kizárólag 
    eszközt keresünk, hanem életérzést is, amelyet egy olcsó, funkcionálisan kiváló darab kevésbé tud megadni.
    <br>
