    Ahogy Sándortól már megszokhattuk, az ergonómia is jól sikerült. A semleges forma 
    számomra minden fogásnemben kényelmes volt, a micarta markolat pedig jó tapadást biztosít, 
    így akár nedves kézzel is biztonságosan dolgozhatunk vele.
