    <h3 class="szoveg1-2">
    Böker Bark Beetle – A susnyásban
    <br>
    <br>
    <h3 class="szoveg2-4">
    Azért a nagy influencerkedés és okoskodás mellett gondoltam, hogy írok egy pár sort arról, hogy mennyire használható a kés. 
    A Böker Bark Beetle meglátásom szerint inkább egy ügyes kis fix, semmint bushcraftkés, így elsősorban kisebb feladatokra való.
    <br>
    <br>
    Ezekben a szerepkörökben kitűnően teljesít, nyársfaragásra, tollaság készítésre, egyéb famunkákra a magam szerény képességeivel 
    is jól tudtam használni. Persze, a név kötelez, egy szútól el is várom, hogy harapja a fát. Óvatosan akár ütőfázni is lehet vele, 
    de a finom él hajlamos hamar kipattogni, így én ezt azért nem ajánlom.
    <br>
    <br>
    A finom, FFG leélezéssel ellátott penge ételfeldolgozásnál is szépen dolgozott, bár ugye a 92mm hosszú penge továbbra sem az ideális 
    szakácskés-méret. Tábori tízóraim egy hiányosságra hívta fel a figyelmet: a 1095 szénacél nagyon hamar patinásodik, a paradicsom mintegy 
    tíz perc alatt látható patinát rajzolt a pengére.
