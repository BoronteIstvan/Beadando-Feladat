    <h3 class="szoveg1">Hogyan történik a Cookie-k kezelése?</h3>
    <br>
    <h3 class="szoveg2">
    Fontos megjegyezni, hogy a weboldal cookie kezeléssel készült. Amennyiben a kliens 
    részlegesen vagy teljes egészében letiltja ezek használatát, megakadályozhatja a weboldal működését. 
    Amennyiben ez így történik, lehetnek olyan funkciók és szolgáltatások, amelyeket nem fog tudni igénybe
    venni teljes, vagy részleges mértékben.
    <br>
    <br>
    A szolgáltatások és használóinak rosszindulatú tevékenységekkel szembeni védelem érdekében akkor is 
    elhelyezünk cookie-kat, ha nincs a Felhasználónak fiókja regisztrálva, de ellátogatott a webhelyünkre. 
    Például ezek a cookie-k segítenek észlelnünk és megakadályoznunk a szolgáltatás megszakítását célzó támadásokat, 
    valamint a hamis fiókok tömeges létrehozását.
    <br>
    <br>
    Ha a böngészőben, vagy a készüléken vannak cookie-k, le tudjuk olvasni az adott cookie-t, amikor közösségi 
    modullal rendelkező weboldalra látogat. A Közösségi honlapok (Facebook, Twitter, LinkedIn, Google+, Reddit, Tumblr) 
    által létrehozott cookie-kért azon oldalakat üzemeltető felel, melyről az adott közösségi oldal honlapján tud informálódni.
